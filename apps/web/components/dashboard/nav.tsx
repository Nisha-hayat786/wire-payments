'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { cn } from '@/lib/utils'

const navItems = [
  { name: 'Overview', href: '/dashboard', icon: HomeIcon },
  { name: 'Invoices', href: '/dashboard/invoices', icon: DocumentIcon },
  { name: 'Payments', href: '/dashboard/payments', icon: CurrencyIcon },
  { name: 'Customers', href: '/dashboard/customers', icon: UsersIcon },
  { name: 'Payouts', href: '/dashboard/payouts', icon: BankIcon },
  { name: 'Developers', href: '/dashboard/developers', icon: CodeIcon },
  { name: 'Settings', href: '/dashboard/settings', icon: CogIcon },
]

export function DashboardNav() {
  const pathname = usePathname()

  return (
    <nav className="hidden md:flex gap-1">
      {navItems.map((item) => {
        const isActive = pathname === item.href || pathname?.startsWith(item.href + '/')
        return (
          <Link
            key={item.href}
            href={item.href}
            className={cn(
              'px-3 py-2 rounded-lg text-sm font-medium transition flex items-center gap-2',
              isActive
                ? 'bg-gray-100 text-gray-900'
                : 'text-gray-600 hover:text-gray-900 hover:bg-gray-50'
            )}
          >
            <item.icon />
            {item.name}
          </Link>
        )
      })}
    </nav>
  )
}

